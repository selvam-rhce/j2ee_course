package dev.selvam.module1.ifCondition;

public class Qus5_AddOrEven {

	public static void main(String[] args) {
		
		int givenNum = 11;
		
		if ( givenNum % 2 == 0 ) {
			System.out.println("Given Number = "+givenNum+" is even");
		}
		else {
			System.out.println("Given Number = "+givenNum+" is odd");
		}

	}

}
